    function selectCombo(category, element) {
        // Desmarcar todos os cards da categoria
        const cards = document.querySelectorAll(`#${category} .selectable-card`);
        cards.forEach(card => {
            card.classList.remove('selected');
        });

        // Marcar o card clicado
        element.classList.add('selected');

        // Atualizar o valor selecionado
        updateTotal();
    }

    function updateTotal() {
    let websiteTotal = 0;
    let socialMediaTotal = 0;

    // Calcula o valor total do combo de website
    document.querySelectorAll('#comboWebsite .selectable-card.selected').forEach(card => {
        websiteTotal += parseFloat(card.getAttribute('data-value').replace(',', '.'));
    });

    // Calcula o valor total do combo de marketing digital multiplicado por 12
    const socialMediaComboValue = document.querySelector('#comboSocialMedia .selectable-card.selected');
    if (socialMediaComboValue) {
        const socialMediaValue = parseFloat(socialMediaComboValue.getAttribute('data-value').replace(',', '.'));
        socialMediaTotal = socialMediaValue * 12;
    }

    // Soma o valor do combo de marketing digital multiplicado e o valor do combo de website
    const totalValue = websiteTotal + socialMediaTotal;

    // Calcula o desconto de 10%
    const discountedTotal = totalValue * 0.94;

    // Calcula o valor da parcela mensal (valor com desconto dividido por 12)
    const monthlyPayment = discountedTotal / 12;

    // Calcula a economia
    const savings = totalValue - discountedTotal;

    // Exibe os valores no card de resultado
    document.getElementById('originalPrice').textContent = `Valor Original: R$ ${totalValue.toFixed(2).replace('.', ',')}`;
    document.getElementById('discountedPrice').textContent = `12X DE R$ ${monthlyPayment.toFixed(2).replace('.', ',')}`;
    document.getElementById('savingsAmount').textContent = `Você economiza: R$ ${savings.toFixed(2).replace('.', ',')} no total`;

    // Adiciona uma descrição explicativa
    document.getElementById('description').textContent = `Você pagará somente R$ ${monthlyPayment.toFixed(2).replace('.', ',')} por mês, durante 1 ano.`;

    // Mostra o card de resultado
    document.getElementById('resultCard').style.display = 'block';
}


